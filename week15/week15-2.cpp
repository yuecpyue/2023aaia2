///week15-2.cpp了解字母對應的值
#include <stdio.h>
int main()
{
    if('A'==65) printf("真的耶,A就是 65\n");
    printf("A的值%d 數字65對應%c\n",'A',65);
    printf("B的值%d 數字66對應%c\n",'B',66);
    printf("C的值%d 數字67對應%c\n",'C',67);
    printf("Z的值%d 數字67對應%c\n",'Z',90);
    printf("\n");
    printf("a的值%d 數字97對應%c\n",'A',97);
    printf("b的值%d 數字98對應%c\n",'B',98);
    printf("c的值%d 數字99對應%c\n",'C',99);
    printf("z的值%d 數字122對應%c\n",'Z',122);
}
